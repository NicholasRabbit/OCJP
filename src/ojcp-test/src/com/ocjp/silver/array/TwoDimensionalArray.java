public class TwoDimensionalArray {

    public static void main(String[] args) {
        /*
        * The first dimension is the number of rows and the second dimension is the number of columns.
        * N.B.
        * 1. The first dimension must be specified while the second dimension is optional.
        * 2. The size of each row does not have to be the same.
        * */
        int[][] arr = new int[2][];
        arr[0] = new int[2];
        arr[1] = new int[3];

    }

}
